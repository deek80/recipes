import React from "react";
import {Typography} from "@material-ui/core";

const DillPickleSoup = () => (
  <div>
    <Typography variant="h4">Ingredients</Typography>
    <ul>
      <li>5-1/2 cups chicken broth</li>
      <li>6-1/2 cups russet potatos, peeled and cubed</li>
      <li>2 cups chopped carrots</li>
      <li>1 cup chopped dill pickles</li>
      <li>1/2 cup unsalted butter</li>
      <li>1/2 cup all-purpose flour</li>
      <li>1 cup sour cream</li>
      <li>1/4 cup water</li>
      <li>2 cups dill pickle juice</li>
      <li>1-1/2 teaspoons Old Bay seasoning</li>
      <li>
        1/2 teaspoon table salt (depending on how salty your pickle juice is)
      </li>
      <li>1/2 teaspoon coarsely ground pepper</li>
      <li>1/4 teaspoon cayenne pepper</li>
    </ul>
    <Typography variant="h4">Directions</Typography>
    <ol>
      <li>
        In a large pot, combine broth, potatoes, carrots and butter. Bring to a
        boil and cook until the potatoes are tender.
      </li>
      <li>Add pickles and continue to boil.</li>
      <li>
        In a medium bowl, stir together flour, sour cream and water, making a
        paste.
      </li>
      <li>
        Whisk sour cream mixture (a couple tablespoons at a time) into soup.
      </li>
      <li>Add pickle juice, Old Bay, salt, pepper and cayenne.</li>
      <li>Cook 5 more minutes and remove from heat.</li>
    </ol>
  </div>
);

export default DillPickleSoup;
